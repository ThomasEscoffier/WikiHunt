/**
 * Conversion de secondes en format HH:MM:SS
 * Ref : https://stackoverflow.com/questions/6312993/javascript-seconds-to-time-string-with-format-hhmmss
 */

String.prototype.toHHMMSS = function () {
    var sec_num = parseInt(this, 10); // don't forget the second param
    var hours = Math.floor(sec_num / 3600);
    var minutes = Math.floor((sec_num - (hours * 3600)) / 60);
    var seconds = sec_num - (hours * 3600) - (minutes * 60);

    if (hours < 10) { hours = "0" + hours; }
    if (minutes < 10) { minutes = "0" + minutes; }
    if (seconds < 10) { seconds = "0" + seconds; }
    return hours + ':' + minutes + ':' + seconds;
}


/**
 * Fonction recursive de recherche de page 
 * 
 * @param {string} categoryName 
 * @param {string[]} [excludedWords]
 * @param {string[]} [foundPages]
 * @param {string[]} [path]
 * @param {int} [minPages]
 */
let fetchCategoryPages = function (categoryName, excludedWords, foundPages, path, minPages) {

    if (typeof excludedWords == 'undefined') {
        excludedWords = [];
    }

    if (typeof foundPages == 'undefined') {
        foundPages = [];
    }

    if (typeof path == 'undefined') {
        path = [];
    }

    if (typeof minPages == 'undefined') {
        minPages = 3;
    }

    /**
     * Enregistrement de passage dans les différentes sous-catégories
     */
    if (path.indexOf(categoryName) === -1) {
        path.push(categoryName);
    }

    return new Promise((resolve, reject) => {
        console.log('QUERY: ' + categoryName)

        if (requestCount >= MAX_FETCH_RANDOM_PAGES_REQUEST_COUNT) {
            alert("Un problème est survenu avec l'API de Wikipedia, merci de réessayer");
            location.href = '';
        }

        /**
         * Analyse des pages sans catégorie et ajout d'1/2 des pages trouvée à la liste des pages potentielles
         * S'il ne trouve pas de page, il choisie une catégorie présente et recherche d'autre pages à l'intérieur
         */

        $.ajax({
            url: wikipediaAPI.searchAllPagesInCategory + categoryName,
            dataType: 'jsonp'
        }).done(function (response) {
            requestCount++;

            foundCategories = [];

            response.query.categorymembers.forEach((categoryMember) => {
                if (!categoryMember.title.startsWith('Catégorie:')) {
                    if (Math.random() * 100 >= 50 && excludedWords.indexOf(categoryMember.title) === -1 && foundPages.indexOf(categoryMember.title) === -1) {
                        foundPages.push(categoryMember.title);
                    }
                } else if (path.indexOf(categoryMember.title.substring(10)) === -1) {
                    foundCategories.push(categoryMember.title.substring(10));
                }
            })


            if (foundPages.length > minPages) {
                return resolve(foundPages);
            }

            let newCategory = path[0];
            if (foundCategories.length > 0) {
                newCategory = foundCategories[Math.floor(Math.random() * foundCategories.length)]
            }

            return resolve(fetchCategoryPages(newCategory, excludedWords, foundPages, path, minPages));
        });
    });
}


/**
 * Créé un timer comptant secondes, minutes et heures.
 **/
function startTimer() {

    startDate = new Date()

    timer = setInterval(function () {
        let timeDifference = new Date() - startDate;
        let seconds = Math.round(timeDifference / 1000);
        let display = seconds.toString().toHHMMSS();

        $("#timer").html(display);

    }, 1000)
}


/**
 * Fonction permettant de récupérer le contenu d'une page Wikipedia et de l'afficher dans le jeu
 * ref : https://www.youtube.com/watch?v=RPz75gcHj18
 **/

function getWikiContent(word) {
    $.ajax({
        url: wikipediaAPI.getContentForPage + word,
        dataType: 'jsonp'
    }).done(function (response) {
        try {
            let html = response.parse.text['*'];
            html = html.replace(/\/\/upload/g, 'https://upload');

            $("#content").html(html);

            $("#content a").each((i, n) => {
                if ((n.href.indexOf('wiki/') === -1) || n.title === '') {
                    n.classList.add('ignored')
                }
            })
            $("#content *").on('click', (e) => {e.preventDefault()});
            $("#content a").on('click', function (e) {
                if (e.target.href !== undefined) {
                    e.preventDefault()

                    clickedWord = e.target.title || e.target.innerText;

                    /**
                     * Condition de victoire
                     **/
                    if (clickedWord.toLowerCase() === endWord.toLowerCase()) {
                        $("#content").html("<div id='win'>Vous avez gagné: " + clickCounter + " clics</div>");
                        
                        clearInterval(Timer);
                        setTimeout(() => {
                            location.href = '';
                        }, 5000)

                        return;
                    }

                    // Vérifie que "wiki/" est présent dans le lien
                    if (e.target.pathname.indexOf("wiki/") !== -1) {
                        getWikiContent(clickedWord);
                        clickCounter += 1;
                        previousWord = currentWord;
                        currentWord = clickedWord;
                        location.hash = decodeURI(e.target.pathname)

                        $("#back").text("Retour vers: " + previousWord)
                        $("#clickCounter").html(clickCounter);
                        $("html, body").animate({ scrollTop: 0 }, "slow");
                        
                        if (clickCounter == 1) {
                            startTimer();
                        }
                    }
                }

            })

        } catch (e) {
            console.error(e);
            console.log('Error');
        }
    });
}

/**
 * Retourne une catégorie sauf celle exclue
 * 
 * @param {string} [excludedCategory]
 * @return {string}
 */
let getCategory = function (excludedCategory) {
    let availableCategories = Object.keys(categories).filter((category) => {
        return category !== excludedCategory;
    })

    return availableCategories[Math.floor(Math.random() * availableCategories.length)];
}

/**
 * Retourne une sous-catégorie d'une catégorie, sauf celle exclue
 * 
 * @param {string} mainCategory
 * @param {string} [excludedCategory]
 * @return {string}
 */
let getSubCategory = function (mainCategory, excludedCategory) {
    let availableSubCategories = categories[mainCategory].filter((subCategory) => {
        return subCategory !== excludedCategory;
    })

    return availableSubCategories[Math.floor(Math.random() * availableSubCategories.length)];
}

